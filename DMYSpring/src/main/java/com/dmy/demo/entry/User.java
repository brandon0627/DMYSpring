package com.dmy.demo.entry;

import com.dmy.framework.annotation.DMYComponent;

@DMYComponent
public class User {
    private String name;

    private int age;

    private String address;

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getAddress() {
        return address;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
